package bd;

public class distrib {

      private String site_id;
      private String name;
      String sponsor;
      String sponsor_code;
      String birthday;
      String address;
      String email;
      String phone;
      String isc_address;
      String flag;


    public String getSite_id() {
        return site_id;
    }

    public void setSite_id(String site_id) {
        this.site_id = site_id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSponsor() {
        return sponsor;
    }

    public void setSponsor(String sponsor) {
        this.sponsor = sponsor;
    }

    public String getSponsor_code() {
        return sponsor_code;
    }

    public void setSponsor_code(String sponsor_code) {
        this.sponsor_code = sponsor_code;
    }

    public String getBirthday() {
        return birthday;
    }

    public void setBirthday(String birthday) {
        this.birthday = birthday;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getIsc_address() {
        return isc_address;
    }

    public void setIsc_address(String isc_address) {
        this.isc_address = isc_address;
    }

    public String getFlag() {
        return flag;
    }

    public void setFlag(String flag) {
        this.flag = flag;
    }



}
